# El math.ceil()método redondea un número hacia arriba hasta su entero más cercano, y el math.floor() método redondea un número hacia abajo hasta su entero más cercano y devuelve el resultado:
x = math.ceil(1.4)
y = math.floor(1.4)

print(x) # returns 2
print(y) # returns 1

print('-----------------------------------------------------')

