# Las funciones min()y max()se pueden utilizar para encontrar el valor más bajo o más alto en un iterable:
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

print('-----------------------------------------------------')

# La abs()función devuelve el valor absoluto (positivo) del número especificado:
x = abs(-7.25)

print(x)

print('-----------------------------------------------------')

